package loopexample;

public class loopExample {
    public static void main(String[] args) {
        int [] k = {10,20,30,40};
        for (int n =0;n< k.length; n++ ){
            System.out.println(k[n]);

        }
        String [] j = {"qwe","fgh"};
        for (int s=0; s< j.length; s++){
            System.out.println(j[s]);

        }
    }
}
